@tool(args_schema=KYCStatusInput)
def check_kyc_status_tool(email: str, refresh_from_veriff: bool = False) -> dict:
    """
    Returns the current KYC verification status for a given user's email.
    Use this tool if the customer is asking about their verification status,
    why their account is on hold, or anything related to KYC.
    """
    return check_kyc_status(email, refresh_from_veriff)


