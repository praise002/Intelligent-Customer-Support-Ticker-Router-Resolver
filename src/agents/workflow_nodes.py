# NODE 1: Generate Response
def generate_response_node(state: TicketState) -> TicketState:
    """
    Uses LLM + RAG context to generate a response.
    """

    llm_with_tools = get_llm()

    customer_email = state.get("email")

    system_prompt = f"""
    You are Bella, a Raenest support agent. You have access to a knowledge base and a set of tools to help customers.
    The customer's email is: {customer_email}

    **Format**  
    - Start: "Hi, thanks for reaching out. I'm Bella."  
    - End: "Best Regards, Bella"  
    - After sign-off, add: "*This message was generated by Bella, our AI Agent*"  

    **Guidelines**  
    - First, decide if you have enough information to answer. If not, use a tool.
    - For KYC-related questions (e.g., "why is my account on hold?", "what's my verification status?"), you MUST use the `check_kyc_status_tool`.
    - After using a tool, use its output to formulate your final answer.
    - If the knowledge base context lacks an answer, state that honestly and suggest escalation.
    - For high urgency (payment delays, account lockouts, missing funds), show empathy and clearly indicate that you will escalate if the issue isn't resolved.  
    - When providing troubleshooting steps, include them from context. Add: "If the issue persists, reply and I'll escalate."  

    **Confidence** (0.0-1.0)  
    - 0.9-1.0: answer directly in context.  
    - 0.7-0.8: confident, may need verification.  
    - 0.5-0.6: low confidence; recommend escalation.  
    - <0.5: cannot answer; escalate.  
    
    Context from knowledge base:
    {state['rag_context']}
    
    Customer issue type: {state['classification']['issue_type']}
    Urgency: {state['classification']['urgency']}

    **Now answer the customer:**
    """

    user_message = f"""Subject: {state['subject']}
    
    Question: {state['description']}
    
    Please provide a helpful response. Also rate your confidence (0.0-1.0) 
    that this answer fully resolves the customer's issue."""

    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=user_message),
    ]

    response = llm.invoke(messages)

    if response.tool_calls:
        print(f"LLM decided to use a tool: {response.tool_calls[0]['name']}")

        tool_call = response.tool_calls[0]
        tool_to_use = {t.name: t for t in tools}[tool_call["name"]]

        tool_output = tool_to_use.invoke(tool_call["args"])

        messages.append(response)
        messages.append(
            ToolMessage(content=str(tool_output), tool_call_id=tool_call["id"])
        )

        print("🗣️ Feeding tool output back to LLM for final response...")
        final_response = llm_with_tools.invoke(messages)

        state["generated_response"] = final_response.content
        # You might want a more robust way to get confidence after a tool call
        state["llm_confidence"] = response.confidence
    else:
        print("🤖 LLM responded directly without using a tool.")
        state["generated_response"] = response.response
        state["llm_confidence"] = response.confidence

    return state


# NODE 2: Calculate Final Confidence
_confidence_calculator = ConfidenceCalculator()


# NODE 2: Calculate Confidence — async because NVIDIA embeddings is async
async def calculate_confidence_node(state: TicketState) -> TicketState:
    """
    Implements the confidence formula:

     confidence = (
         0.4 * retrieval_score +
         0.3 * semantic_similarity +
         0.3 * llm_self_score
     )

     Updates state with:
     - semantic_similarity
     - final_confidence
    """
    query = f"{state['subject']}. {state['description']}"

    semantic_similarity = await _confidence_calculator.calculate_similarity(
        query, state["rag_context"]
    )

    final_confidence = (
        0.4 * state["retrieval_score"]
        + 0.3 * semantic_similarity
        + 0.3 * state["llm_confidence"]
    )

    state["semantic_similarity"] = semantic_similarity
    state["final_confidence"] = final_confidence

    return state


